    private EjbJar module;


    public CustomizerRunEjb(ModelHandle2 handle, Project project) {
        super(handle, project, J2eeModule.Type.EJB);
        initComponents();

        module = EjbJar.getEjbJar(project.getProjectDirectory());
        if (module != null) {
            String profile = module.getJ2eeProfile().getDisplayName();
            txtJ2EEVersion.setText(profile);
        }

        initDeployOnSave(jCheckBoxDeployOnSave, dosDescription);
        initServerModel(comServer, lblServer);
    }

    @Override
    public void applyChangesInAWT() {
        Object obj = comServer.getSelectedItem();
        if (obj != null) {
            LoggingUtils.logUsage(CustomizerRunEjb.class, "USG_PROJECT_CONFIG_MAVEN_SERVER", new Object[] {obj.toString()}, "maven"); //NOI18N
        }
    }

    @Override
    public void applyChanges() {
        serverUpdater.storeValue();
        deployOnSaveUpdater.storeValue();
    }

