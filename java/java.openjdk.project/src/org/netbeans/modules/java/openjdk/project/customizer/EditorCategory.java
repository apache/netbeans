    private ListCellRenderer<Object> useRemotePlatformRenderer() {
        return new DefaultListCellRenderer() {
            @Override
            public Component getListCellRendererComponent(JList<?> list, Object value, int index, boolean isSelected, boolean cellHasFocus) {
                if (value instanceof Settings.UseRemotePlatform) {
                    value = useRemotePlatformDisplayName.getOrDefault(value, String.valueOf(value));
                }
                return super.getListCellRendererComponent(list, value, index, isSelected, cellHasFocus);
            }
        };
    }

    private ComboBoxModel<UseRemotePlatform> useRemotePlatformModel() {
        DefaultComboBoxModel<UseRemotePlatform> model = new DefaultComboBoxModel<>();

        for (UseRemotePlatform rb : UseRemotePlatform.values()) {
            model.addElement(rb);
        }

        return model;
    }

    private static final Map<UseRemotePlatform, String> useRemotePlatformDisplayName = new EnumMap<UseRemotePlatform, String>(UseRemotePlatform.class) {{
        put(UseRemotePlatform.ALWAYS, "Always");
        put(UseRemotePlatform.JAVA_COMPILER, "For java.compiler module only");
        put(UseRemotePlatform.NEVER, "Never");
    }};

    @CompositeCategoryProvider.Registration(projectType=JDKProject.PROJECT_KEY, position=300)
    public static CompositeCategoryProvider createCategoryProvider() {
        return new CompositeCategoryProvider() {
            @Override
            public Category createCategory(Lookup context) {
                if (context.lookup(Settings.class) != null)
                    return Category.create("editor", "Editor", null);
                return null;
            }
            @Override
            public JComponent createComponent(Category category, Lookup context) {
                Settings settings = context.lookup(Settings.class);
                EditorCategory panel = new EditorCategory(settings.getUseRemotePlatform());
                category.setOkButtonListener(evt -> {
                    settings.setUseRemotePlatform((UseRemotePlatform) panel.useRemotePlatform.getSelectedItem());
                });
                category.setStoreListener(evt -> settings.flush());
                return panel;
            }
        };
    }
