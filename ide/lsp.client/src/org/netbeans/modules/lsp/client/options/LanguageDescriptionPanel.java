    public LanguageDescriptionPanel(LanguageDescription desc, Set<String> usedIds) {
        initComponents();
        if (desc == null) {
            String id;
            String base = "";
            OUTER: while (true) {
                for (char c = 'a'; c <= 'z'; c++) {
                    if (usedIds.add(base + c)) {
                        id = base + c;
                        break OUTER;
                    }
                }
                base = "a";
            }
            this.id = id;
        } else {
            this.id = desc.id;
            this.extensions.setText(desc.extensions);
            this.syntax.setText(desc.syntaxGrammar);
            this.server.setText(desc.languageServer);
            this.name.setText(desc.name);
            this.icon.setText(desc.icon);
            this.debugger.setSelected(desc.debugger);
            this.languageId.setText(desc.languageId);
        }
    }

    public LanguageDescription getDescription() {
        return new LanguageDescription(
                id,
                this.extensions.getText(),
                this.syntax.getText(),
                this.server.getText(),
                this.name.getText(),
                this.icon.getText(),
                this.debugger.isSelected(),
                this.languageId.getText()
        );
    }

