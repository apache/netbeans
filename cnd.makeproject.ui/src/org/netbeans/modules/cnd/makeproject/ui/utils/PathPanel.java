    private void setMode(MakeProjectOptions.PathMode mode) {
	MakeProjectOptions.setPathMode(mode);
        switch (mode) {
            case ABS:
                absRadioButton.setSelected(true);
                break;
            case REL:
                relRadioButton.setSelected(true);
                break;
            case REL_OR_ABS:
                relOrAbsRadioButton.setSelected(true);
                break;
        }
    }
    
