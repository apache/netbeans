    public CategoryNamePanel(String lblNameText, String txtNameText) {
        initComponents();
        lblName.setText(lblNameText);
        if (!txtNameText.isEmpty()) {
            txtName.setText(txtNameText);
            txtName.selectAll();
        }
    }

    public String getCategoryName() {
        return txtName.getText();
    }

    void addDocumentListener(DocumentListener listener) {
        txtName.getDocument().addDocumentListener(listener);
    }

    void removeDocumentListener(DocumentListener listener) {
        txtName.getDocument().removeDocumentListener(listener);
    }

    void setErrorText(String message) {
        if (message.isEmpty()) {
            lblError.setIcon(null);
        } else {
            lblError.setIcon(ImageUtilities.loadImageIcon("org/netbeans/modules/bugtracking/tasks/resources/error.png", true)); //NOI18N
        }
        lblError.setText(message);
    }

