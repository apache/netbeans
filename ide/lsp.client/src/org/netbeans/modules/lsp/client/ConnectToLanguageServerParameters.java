    private static final String KEY_ROOT = "root";
    private static final String KEY_PORT = "port";
    private static final String KEY_EXTENSIONS = "extensions";
    private final Preferences settings;

    public ConnectToLanguageServerParameters() {
        initComponents();
        Preferences prefs = NbPreferences.forModule(ConnectToLanguageServerParameters.class);
        settings = prefs.node("connect");
        root.setText(settings.get(KEY_ROOT, ""));
        port.setText(settings.get(KEY_PORT, ""));
        extensions.setText(settings.get(KEY_EXTENSIONS, ""));
    }

    public void confirm() {
        settings.put(KEY_ROOT, root.getText());
        settings.put(KEY_PORT, port.getText());
        settings.put(KEY_EXTENSIONS, extensions.getText());
    }

    public String getExtensions() {
        return extensions.getText();
    }

    public String getPort() {
        return port.getText();
    }

    public String getRoot() {
        return root.getText();
    }

